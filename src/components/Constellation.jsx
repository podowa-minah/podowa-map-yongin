// 첫 별을 alpha(주성)으로 살짝 크게
const ALPHA_STAR_R = 1.8;
const STAR_R = 1.1;

export default function Constellation({ signName, size = 40, color = '#1f2937', lineOpacity = 0.4 }) {
  const data = DATA[signName];
  if (!data) return null;
  return (
    <svg viewBox="0 0 40 40" width={size} height={size} aria-hidden="true">
      {data.lines.map(([a, b], i) => {
        const [x1, y1] = data.stars[a];
        const [x2, y2] = data.stars[b];
        return (
          <line key={i}
            x1={x1} y1={y1} x2={x2} y2={y2}
            stroke={color} strokeWidth="0.5" opacity={lineOpacity} strokeLinecap="round" />
        );
      })}
      {data.stars.map(([x, y], i) => (
        <circle key={i} cx={x} cy={y}
          r={i === 0 ? ALPHA_STAR_R : STAR_R}
          fill={color} />
      ))}
    </svg>
  );
}
